    for inputs in TLdata_loader.get_batches(data, batch_size, False):
        X, X2, X3, X4, Y = inputs[0], inputs[1], inputs[2], inputs[3], inputs[4]
        output,_ = TLmodel(X, X2, X3, X4)
        loss_train = F.l1_loss(output, Y) # mse_loss
        total_loss += loss_train.item()
        n_samples += (output.size(0) * TLdata_loader.m);

        y_true_mx.append(Y.data.cpu())
        y_pred_mx.append(output.data.cpu())

    y_pred_mx = torch.cat(y_pred_mx)
    y_true_mx = torch.cat(y_true_mx) # [n_samples, 47] 
    y_true_states = y_true_mx.numpy() * (TLdata_loader.max - TLdata_loader.min ) * 1.0 + TLdata_loader.min  
    y_pred_states = y_pred_mx.numpy() * (TLdata_loader.max - TLdata_loader.min ) * 1.0 + TLdata_loader.min  #(#n_samples, 47)
    rmse_states = np.mean(np.sqrt(mean_squared_error(y_true_states, y_pred_states, multioutput='raw_values'))) # mean of 47
    raw_mae = mean_absolute_error(y_true_states, y_pred_states, multioutput='raw_values')
