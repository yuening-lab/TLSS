def train(TLdata_loader, data):
    #print(TLmodel.conv.weight)
    TLmodel.train()
    total_loss = 0.
    n_samples = 0.
    batch_size = args.batch

    for inputs in TLdata_loader.get_batches(data, batch_size, True):
        X, X2, X3, X4, Y = inputs[0], inputs[1], inputs[2], inputs[3], inputs[4]
        optimizer.zero_grad()
        #optimizer.requires_grad = True
        output,_ = TLmodel(X, X2, X3, X4) 
        if Y.size(0) == 1:
            Y = Y.view(-1)
        loss_train = F.l1_loss(output, Y) # mse_loss
        total_loss += loss_train.item()
        loss_train.backward()
        optimizer.step()
        n_samples += (output.size(0) * TLdata_loader.m)
    return float(total_loss / n_samples)
 
